    private void btn_bitacoraContenidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bitacoraContenidoActionPerformed
    try {
        // Obtén la ruta absoluta del archivo "Historial.txt"
        String filePath = "Historial.txt";
        // Crea un objeto File con la ruta del archivo
        File file = new File(filePath);
        // Verifica si el archivo existe y es un archivo
        if (file.exists() && file.isFile()) {
            // Abre el archivo con el programa predeterminado
            Desktop.getDesktop().open(file);
        } else {
            System.out.println("El archivo no existe o no es un archivo válido.");
        }
    } catch (IOException ex) {
        ex.printStackTrace();
        System.out.println("Error al intentar abrir el archivo.");
    }
       
    }//GEN-LAST:event_btn_bitacoraContenidoActionPerformed

