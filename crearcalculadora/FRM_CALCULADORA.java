    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
       this.segundonumero=Float.parseFloat(this.lbl_entrada.getText());
       switch(this.operador){
           case "+":this.lbl_entrada.setText(sincero(this.primernumero + this.segundonumero));
       }
       this.segundonumero=Float.parseFloat(this.lbl_entrada.getText());
       switch(this.operador){
           case "-":this.lbl_entrada.setText(sincero(this.primernumero - this.segundonumero));
       }
       this.segundonumero=Float.parseFloat(this.lbl_entrada.getText());
       switch(this.operador){
           case "*":this.lbl_entrada.setText(sincero(this.primernumero * this.segundonumero));
       }
       this.segundonumero=Float.parseFloat(this.lbl_entrada.getText());
       switch(this.operador){
           case "/":this.lbl_entrada.setText(sincero(this.primernumero / this.segundonumero));
       }
       objeto.Bitacora(" = " + this.lbl_entrada.getText()+"\n");
    }//GEN-LAST:event_btn_igualActionPerformed

